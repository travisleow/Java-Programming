public class Cake {
    String flavor;
    double weight;
    
    public Cake(String flavor, double weight) {
        this.flavor = flavor;
        this.weight = weight;
    }
    
    public String getFlavor() {
        return this.flavor;
    }
    
    public double getWeight() {
        return this.weight;
    }
}
